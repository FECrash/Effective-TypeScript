/**
 * never, unknown, any 타입에 대해 정리하여 최대한 상세하게 적어주세요.
 */

// never 는 가장 작은 단위의 타입입니다. never 타입에는 어떤 타입이든 할당 할 수 없습니다. any도 막는다

// unknown 은 모든 타입의 상위타입입니다. unknown 타입에 어느 타입이든 할당 할 수 있으나, 다른 타입으로 단언까지 해주는 것이 좋습니다. unknown 이후 단언으로 타입을 설정해줄 수 있지만 unknown 및 단언으로 인해 불안정할 수 있습니다.

// any는 타입스크립트가 자바스크립트의 superset으로 이후 출시되었기에 기존 자바스크립트 프로젝트를 타입스크립트로 리팩토링할때에 유용하게 사용 할 수 있는 타입입니다. 
// 어떠한 타입이든지 올 수 있다는 가정이기에 당장 타입에러를 고치는데는 좋을지 모르지만 이 후 타입을 유추할 때에 애를 먹을 수 있습니다.